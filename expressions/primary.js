// Literal Values

'Hello World.' // A string literal
100 // A number literal

// Variable references
book // Evaluates to the value of book
fullName // Evaluates to the value of fullName
undefined // Evaluates to the value of the global variable undefined which is the value undefined.

// Keywords
this // Evaluates to the current object
true // Evaluates to the boolean true
false // Evaluates to the boolean false
null // Evaluates to the null value
