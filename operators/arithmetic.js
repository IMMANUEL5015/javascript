// Addition
console.log(10 + 40 + 20) //70

// Subtraction
console.log(50 - 20) // 30

// Multiplication
console.log(10 * 2 * 3); // 60

// Division
console.log(20 / 10); // 2

// Modulo
console.log(20 % 3) // 2

// Unary Plus
console.log(+"3.5") // 3.5
console.log(+false) // 0
console.log(+"abc") // NaN

// Unary Negation
console.log(-3.5) // -3.5
console.log(-"3.5") // 3.5
console.log(-false) // 0
console.log(-"abc") // NaN

// Increment (Pre-Increment)
let num = 1;
console.log(++num); // 2
console.log(num); // 2

// Increment (Post-Increment)
let x = 1;
console.log(x++); // 1
console.log(x); // 2

// Decrement (Pre-Decrement)
let y = 1;
console.log(--y); // 0
console.log(y); // 0

// Decrement (Post-Decrement)
let z = 1;
console.log(z--); // 1
console.log(z); // 0