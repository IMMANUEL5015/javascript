// Basic Assignment
const a = 1;
console.log(a) // 1

const arr = [];
arr[0] = 1;
console.log(arr); // [1];

const obj = {};
obj.a = 1;
console.log(obj); // {a: 1}

// Addition Assignment
let b = 2;
b+=2;
console.log(b); // 4

// Subtraction Assignment
let c = 5;
c-=3;
console.log(c); // 2

// Multiplication Assignment
let d = 4;
d*=2;
console.log(d); // 8

// Division Assignment
let e = 3;
e*=2;
console.log(e); // 6

// Modulo Operator
let f = 10;
f%=3;
console.log(f); // 1